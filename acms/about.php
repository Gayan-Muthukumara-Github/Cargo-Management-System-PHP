<script>
    $(function(){
        // Add animations on scroll
        $(window).scroll(function() {
            $('.stat-card, .team-card').each(function() {
                var elementTop = $(this).offset().top;
                var elementBottom = elementTop + $(this).outerHeight();
                var viewportTop = $(window).scrollTop();
                var viewportBottom = viewportTop + $(window).height();
                
                if (elementBottom > viewportTop && elementTop < viewportBottom) {
                    $(this).addClass('fade-in');
                }
            });
        });
        
        // Add hover effects to cards
        $('.stat-card, .team-card').hover(
            function() {
                $(this).find('.stat-number, .team-avatar').addClass('animate__animated animate__pulse');
            },
            function() {
                $(this).find('.stat-number, .team-avatar').removeClass('animate__animated animate__pulse');
            }
        );
        
        // Smooth scrolling for anchor links
        $('a[href^="#"]').on('click', function(event) {
            var target = $(this.getAttribute('href'));
            if( target.length ) {
                event.preventDefault();
                $('html, body').stop().animate({
                    scrollTop: target.offset().top - 80
                }, 1000);
            }
        });
    });
</script>